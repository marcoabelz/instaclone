import {gql} from '@apollo/client'

export const POST = gql`
  mutation CreatePost($newPost: NewPost) {
    createPost(newPost: $newPost) {
      _id
      content
      tags
      imgUrl
      authorId
      comments {
        content
        username
        createdAt
        updatedAt
      }
      likes {
        username
        createdAt
        updatedAt
      }
      createdAt
      updatedAt
      author {
        _id
        name
        username
        email
      }
    }
  }
`;

